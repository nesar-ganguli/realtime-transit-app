const fs = require('fs');
const zlib = require('zlib');
const path = require('path');

function createCirclePNG(size, hexColor) {
  const r = parseInt(hexColor.slice(0, 2), 16);
  const g = parseInt(hexColor.slice(2, 4), 16);
  const b = parseInt(hexColor.slice(4, 6), 16);

  const cx = size / 2;
  const outerR = size / 2 - 0.5;
  const border = Math.max(1, size / 8);
  const innerR = outerR - border;

  const pixels = new Uint8Array(size * size * 4);

  for (let y = 0; y < size; y++) {
    for (let x = 0; x < size; x++) {
      const dx = x + 0.5 - cx;
      const dy = y + 0.5 - cx;
      const dist = Math.sqrt(dx * dx + dy * dy);
      const idx = (y * size + x) * 4;

      if (dist <= outerR) {
        if (dist >= innerR) {
          // White border
          pixels[idx] = 0xff; pixels[idx+1] = 0xff;
          pixels[idx+2] = 0xff; pixels[idx+3] = 255;
        } else {
          // Route colour fill
          pixels[idx] = r; pixels[idx+1] = g;
          pixels[idx+2] = b; pixels[idx+3] = 255;
        }
      }
    }
  }

  return encodePNG(size, size, pixels);
}

function encodePNG(W, H, pixels) {
  const crcTable = new Uint32Array(256);
  for (let i = 0; i < 256; i++) {
    let c = i;
    for (let j = 0; j < 8; j++) c = (c & 1) ? (0xedb88320 ^ (c >>> 1)) : (c >>> 1);
    crcTable[i] = c;
  }
  function crc32(buf) {
    let crc = 0xffffffff;
    for (const b of buf) crc = crcTable[(crc ^ b) & 0xff] ^ (crc >>> 8);
    return (crc ^ 0xffffffff) >>> 0;
  }
  function u32(v) { const b = Buffer.alloc(4); b.writeUInt32BE(v); return b; }
  function chunk(type, data) {
    const t = Buffer.from(type, 'ascii');
    return Buffer.concat([u32(data.length), t, data, u32(crc32(Buffer.concat([t, data])))]);
  }

  const ihdr = Buffer.concat([u32(W), u32(H), Buffer.from([8, 6, 0, 0, 0])]);
  const raw  = Buffer.alloc(H * (1 + W * 4));
  for (let y = 0; y < H; y++) {
    raw[y * (1 + W * 4)] = 0;
    for (let x = 0; x < W; x++) {
      const s = (y * W + x) * 4, d = y * (1 + W * 4) + 1 + x * 4;
      raw[d]=pixels[s]; raw[d+1]=pixels[s+1]; raw[d+2]=pixels[s+2]; raw[d+3]=pixels[s+3];
    }
  }
  return Buffer.concat([
    Buffer.from([137,80,78,71,13,10,26,10]),
    chunk('IHDR', ihdr),
    chunk('IDAT', zlib.deflateSync(raw, { level: 9 })),
    chunk('IEND', Buffer.alloc(0)),
  ]);
}

// All unique route colors from Bloomington Transit GTFS
const COLORS = [
  'EC0E0E','035EBF','239504','B505F5','F50A68',
  '80471C','918800','04B1DC','037693','869A3A',
  'CC8100','5A5A5A','AF67B4','1565C0',
];

const outDir  = path.join(__dirname, '..', 'assets', 'stop-markers');
const genDir  = path.join(__dirname, '..', 'src', 'generated');

COLORS.forEach(hex => {
  fs.writeFileSync(path.join(outDir, `${hex}.png`),    createCirclePNG(12, hex));
  fs.writeFileSync(path.join(outDir, `${hex}@2x.png`), createCirclePNG(24, hex));
  fs.writeFileSync(path.join(outDir, `${hex}@3x.png`), createCirclePNG(36, hex));
});

// Generate static require() mapping (Metro needs static requires)
const lines = COLORS.map(
  hex => `  '${hex}': require('../../assets/stop-markers/${hex}.png'),`
);
const content = `// Auto-generated by scripts/generate-stop-marker.js
const stopMarkerImages = {
${lines.join('\n')}
};
export default stopMarkerImages;
`;
fs.writeFileSync(path.join(genDir, 'stopMarkerImages.js'), content);
console.log(`Generated ${COLORS.length} stop marker sets + stopMarkerImages.js`);
